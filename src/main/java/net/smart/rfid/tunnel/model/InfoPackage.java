package net.smart.rfid.tunnel.model;

public class InfoPackage {

	
	private int sku;

	private int pack;
	
	private int section;
	
	private int brand;

	public InfoPackage() {
	}

	public int getSku() {
		return sku;
	}

	public void setSku(int sku) {
		this.sku = sku;
	}

	public int getPack() {
		return pack;
	}

	public void setPack(int pack) {
		this.pack = pack;
	}

	public int getSection() {
		return section;
	}

	public void setSection(int section) {
		this.section = section;
	}

	public int getBrand() {
		return brand;
	}

	public void setBrand(int brand) {
		this.brand = brand;
	}

	

	

}
